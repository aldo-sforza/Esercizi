namespace Calculator.Model
{
    public class Calculator
    {
        public int Add(int v1, int v2)
        {
            return v1+v2;
        }

        public float AddDec(float v1, float v2)
        {
            return v1 + v2;
        }

        public int Sub(int v1, int v2) 
        { 
            return v1 - v2;
        }
    }
}